<?php

namespace App\Services\Social;

/**
 * Монгол нэрээр хүйс ТААМАГЛАХ heuristic.
 *
 * ⚠️ Энэ нь 100% БИШ — Meta нь чатын хэрэглэгчийн хүйсийг өгдөггүй тул
 * ойролцоо таамаглал хийж самбарын статистикийг бөглөнө. Оператор inbox-оос
 * гараар засаж болно. Тодорхойгүй бол null буцаана (таамаглахгүй).
 */
class GenderGuesser
{
    /**
     * Эмэгтэй нэрэнд элбэг тохиолддог онцлог хэсгүүд (Cyrillic).
     * Хоёрдмол/хэт ерөнхий (нар, сар, уран, дэлгэр г.м.) үгсийг ХАСсан — буруу таахаас
     * илүү null (тодорхойгүй) байх нь дээр.
     */
    private const FEMALE = [
        'цэцэг', 'оюун', 'сувд', 'болор', 'сарнай', 'дулам', 'дулмаа', 'туяа', 'чимэг',
        'должин', 'долгор', 'дарь', 'навч', 'золзаяа', 'намуун', 'энхжин',
        'солонго', 'найра', 'мядаг', 'должмаа', 'гэрэлмаа', 'отгонцэцэг', 'алтанцэцэг',
        'эрдэнэцэцэг', 'наранцэцэг', 'мөнгөнцэцэг',
    ];

    /** Эрэгтэй нэрэнд элбэг тохиолддог хэсгүүд (Cyrillic). */
    private const MALE = [
        'баатар', 'болд', 'дорж', 'төмөр', 'очир', 'пүрэв', 'батбаяр', 'ганбат',
        'ганболд', 'батболд', 'сүх', 'чулуун', 'хүчит', 'батаа', 'мөнхбат', 'наранбаатар',
        'гомбо', 'лувсан', 'дамдин', 'нямбуу', 'ширнэн', 'дэмбэрэл', 'жавхлан', 'тэмүүлэн',
        'тэмүүжин', 'ганхүү', 'батхүү', 'мөнххүү', 'эрхэмбаяр', 'батжаргал', 'батсайхан',
        'батэрдэнэ', 'батмөнх', 'ариунбат', 'хосбаяр',
    ];

    /** Нэрнээс хүйс таамаглах: 'male' / 'female' / null. */
    public function guess(?string $name): ?string
    {
        $n = mb_strtolower(trim((string) $name));
        if ($n === '') {
            return null;
        }

        // -маа төгсгөлтэй (Болормаа, Дэлгэрмаа г.м.) — голдуу эмэгтэй.
        if (str_ends_with($n, 'маа')) {
            return 'female';
        }

        // Эмэгтэйн хүчтэй тэмдэг эрэгтэйнхээс түрүүнд (эмэгтэй нэр илүү онцлог).
        foreach (self::FEMALE as $kw) {
            if (str_contains($n, $kw)) {
                return 'female';
            }
        }
        foreach (self::MALE as $kw) {
            if (str_contains($n, $kw)) {
                return 'male';
            }
        }

        return null; // тодорхойгүй
    }
}
